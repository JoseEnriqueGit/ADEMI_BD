PROMPT === 2. Filas exclusivas en PR_REPRESTAMOS (esperado: 0) ===
WITH A AS (
    SELECT CODIGO_EMPRESA, CODIGO_CLIENTE, NO_CREDITO, FECHA_CORTE, ESTADO
      FROM OPTACUM_SNAP_REPRESTAMOS
     WHERE RUN_LABEL = UPPER('&RUN_LABEL_BASE')
),
D AS (
    SELECT CODIGO_EMPRESA, CODIGO_CLIENTE, NO_CREDITO, FECHA_CORTE, ESTADO
      FROM OPTACUM_SNAP_REPRESTAMOS
     WHERE RUN_LABEL = UPPER('&RUN_LABEL_TEST')
)
SELECT 'SOLO_EN_' || UPPER('&RUN_LABEL_TEST') AS ORIGEN,
       D.CODIGO_EMPRESA,
       D.CODIGO_CLIENTE,
       D.NO_CREDITO,
       D.FECHA_CORTE,
       D.ESTADO
  FROM D
 WHERE NOT EXISTS (
           SELECT 1
             FROM A
            WHERE A.CODIGO_EMPRESA = D.CODIGO_EMPRESA
              AND A.CODIGO_CLIENTE = D.CODIGO_CLIENTE
              AND A.NO_CREDITO = D.NO_CREDITO
              AND A.FECHA_CORTE = D.FECHA_CORTE
       )
UNION ALL
SELECT 'SOLO_EN_' || UPPER('&RUN_LABEL_BASE'),
       A.CODIGO_EMPRESA,
       A.CODIGO_CLIENTE,
       A.NO_CREDITO,
       A.FECHA_CORTE,
       A.ESTADO
  FROM A
 WHERE NOT EXISTS (
           SELECT 1
             FROM D
            WHERE D.CODIGO_EMPRESA = A.CODIGO_EMPRESA
              AND D.CODIGO_CLIENTE = A.CODIGO_CLIENTE
              AND D.NO_CREDITO = A.NO_CREDITO
              AND D.FECHA_CORTE = A.FECHA_CORTE
       );

