DEFINE RUN_LABEL_BASE = ANTES
DEFINE RUN_LABEL_TEST = DESPUES

PROMPT
PROMPT === 1. Resumen de runs ===
SELECT RUN_LABEL,
       TEST_USER,
       STARTED_AT,
       FINISHED_AT,
       LOTE_ORIGINAL,
       LOTE_PRUEBA,
       RE_INSERTADOS,
       SOLICITUDES,
       CANALES,
       OPCIONES,
       BITACORA,
       NOTAS
  FROM OPTACUM_RUN_CONTROL 
 WHERE RUN_LABEL IN (UPPER('&RUN_LABEL_BASE'), UPPER('&RUN_LABEL_TEST'))
 ORDER BY RUN_LABEL;

