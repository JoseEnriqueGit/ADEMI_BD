                  AND D.NO_CREDITO = A.NO_CREDITO
                  AND D.FECHA_CORTE = A.FECHA_CORTE
           )
    UNION
    SELECT CODIGO_EMPRESA,
           CODIGO_CLIENTE,
           NO_CREDITO,
           FECHA_CORTE
      FROM OPTACUM_SNAP_REPRESTAMOS D
     WHERE RUN_LABEL = UPPER('&RUN_LABEL_TEST')
       AND NOT EXISTS (
               SELECT 1
                 FROM OPTACUM_SNAP_REPRESTAMOS A
                WHERE A.RUN_LABEL = UPPER('&RUN_LABEL_BASE')
                  AND A.CODIGO_EMPRESA = D.CODIGO_EMPRESA
                  AND A.CODIGO_CLIENTE = D.CODIGO_CLIENTE
                  AND A.NO_CREDITO = D.NO_CREDITO
                  AND A.FECHA_CORTE = D.FECHA_CORTE
           )
),
MAPEO AS (
    SELECT R.RUN_LABEL,
           R.CODIGO_EMPRESA,
           R.CODIGO_CLIENTE,
           R.NO_CREDITO,
           R.FECHA_CORTE,
           R.ID_REPRESTAMO
      FROM OPTACUM_SNAP_REPRESTAMOS R
      JOIN EXCLUSIVOS X
        ON X.CODIGO_EMPRESA = R.CODIGO_EMPRESA
       AND X.CODIGO_CLIENTE = R.CODIGO_CLIENTE
       AND X.NO_CREDITO = R.NO_CREDITO
       AND X.FECHA_CORTE = R.FECHA_CORTE
)
SELECT M.RUN_LABEL,
       M.CODIGO_EMPRESA,
       M.CODIGO_CLIENTE,
       M.NO_CREDITO,
       M.FECHA_CORTE,
       (SELECT COUNT(*)
          FROM OPTACUM_SNAP_SOLICITUD S
         WHERE S.RUN_LABEL = M.RUN_LABEL
           AND S.CODIGO_EMPRESA = M.CODIGO_EMPRESA
           AND S.ID_REPRESTAMO = M.ID_REPRESTAMO) AS CNT_SOLICITUD,
       (SELECT COUNT(*)
          FROM OPTACUM_SNAP_CANALES C
         WHERE C.RUN_LABEL = M.RUN_LABEL
           AND C.CODIGO_EMPRESA = M.CODIGO_EMPRESA
           AND C.ID_REPRESTAMO = M.ID_REPRESTAMO) AS CNT_CANALES,
       (SELECT COUNT(*)
          FROM OPTACUM_SNAP_OPCIONES O
         WHERE O.RUN_LABEL = M.RUN_LABEL
           AND O.CODIGO_EMPRESA = M.CODIGO_EMPRESA
           AND O.ID_REPRESTAMO = M.ID_REPRESTAMO) AS CNT_OPCIONES,
       (SELECT COUNT(*)
          FROM OPTACUM_SNAP_BITACORA B
         WHERE B.RUN_LABEL = M.RUN_LABEL
           AND B.CODIGO_EMPRESA = M.CODIGO_EMPRESA
           AND B.ID_REPRESTAMO = M.ID_REPRESTAMO) AS CNT_BITACORA
  FROM MAPEO M
 ORDER BY M.CODIGO_EMPRESA, M.CODIGO_CLIENTE, M.NO_CREDITO, M.RUN_LABEL;

