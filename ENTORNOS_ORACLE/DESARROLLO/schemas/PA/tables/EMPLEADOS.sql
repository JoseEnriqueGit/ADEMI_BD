  CREATE OR REPLACE EDITIONABLE TRIGGER "PA"."TGR_VALIDA_CART_ACTIVA_EMPL" 
BEFORE INSERT OR UPDATE OF COD_AGENCIA,
                           COD_AGENCIA_LABORA ON "PA"."EMPLEADOS" 
REFERENCING OLD AS OLD NEW AS NEW 
FOR EACH ROW 
BEGIN
  IF  (:NEW.COD_AGENCIA != :OLD.COD_AGENCIA OR :NEW.COD_AGENCIA_LABORA != :OLD.COD_AGENCIA_LABORA) 
  AND (NVL(:OLD.ESTA_ACTIVO, 'X') = 'S' AND NVL(:OLD.ES_OFICIAL, 'X') = 'S') THEN
    CASE F_PA_VALIDA_CARTERA_ACTIVA(:OLD.ID_EMPLEADO)
        WHEN 1 THEN
            RAISE_APPLICATION_ERROR(-20001, 'No se puede cambiar de agencia a empleado con cartera activa en créditos.');
        WHEN 2 THEN
            RAISE_APPLICATION_ERROR(-20001, 'No se puede cambiar de agencia a empleado con cartera activa en tarjetas.');
        WHEN 3 THEN
            RAISE_APPLICATION_ERROR(-20001, 'No se puede cambiar de agencia a empleado con cartera activa en créditos y tarjetas.');
        ELSE
            NULL;
    END CASE;
  END IF;
END;
