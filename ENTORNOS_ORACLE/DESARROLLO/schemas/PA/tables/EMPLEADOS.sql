  CREATE OR REPLACE EDITIONABLE TRIGGER "PA"."BUI_EMPLEADOS" 
BEFORE INSERT OR UPDATE
ON pa.empleados REFERENCING NEW AS new OLD AS Old
FOR EACH ROW
BEGIN
  -- Se valida si hay cambios en el estado o en es_oficial.
  IF UPDATING THEN
     IF ((nvl(:old.es_oficial,'x') != nvl(:new.es_oficial,'x')) OR  
          (nvl(:old.esta_Activo,'x') != nvl(:new.esta_activo,'x'))
        ) THEN
        INSERT INTO pa_bit_cambios_empleado
           (COD_EMPRESA,       ID_EMPLEADO,      ES_OFICIAL_ANT,  ES_OFICIAL_ACT,  ESTA_ACTIVO_ANT,  ESTA_ACTIVO_ACT, FECHA_CAMBIO)
         VALUES
            (:new.cod_empresa, :new.id_empleado, :old.es_oficial, :new.es_oficial, :old.esta_Activo, :new.esta_Activo,sysdate);
     END IF;
  END IF;
END;
