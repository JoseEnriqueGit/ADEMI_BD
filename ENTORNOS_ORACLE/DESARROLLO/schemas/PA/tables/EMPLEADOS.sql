  CREATE OR REPLACE EDITIONABLE TRIGGER "PA"."EMPLEADOS_EMPI2000" 
       after insert or delete
       on PA.EMPLEADOS
       referencing new as new old as old
       for each row
DECLARE
  -- Obtiene un indicador de si el empleado es cliente o no
  cursor cliente(pCodEmpleado in varchar2) is
    select 'x'
      from clientes
      where cod_cliente = pCodEmpleado;
  vDummy varchar2(1);
BEGIN
  if inserting then
    open cliente(:new.cod_per_fisica);
      fetch cliente into vDummy;
      if cliente%found then  -- Si es Cliente y Empleado
        update tcli_personas
          set cli_ind_relacion = 'A'
          where cli_cod_persona = :new.cod_per_fisica;
      else  -- Si es solo Empleado
        update tcli_personas
          set cli_ind_relacion = 'E'
          where cli_cod_persona = :new.cod_per_fisica;
      end if;
    close cliente;
  elsif deleting then
    open cliente(:new.cod_per_fisica);
      fetch cliente into vDummy;
      if cliente%found then -- Si va a ser solo Cliente
        update tcli_personas
          set cli_ind_relacion = 'C'
          where cli_cod_persona = :new.cod_per_fisica;
      else  -- Si no va a ser Cliente ni Empleado, entonces es Prospecto
        update tcli_personas
          set cli_ind_relacion = 'P'
          where cli_cod_persona = :new.cod_per_fisica;
      end if;
    close cliente;
  end if;  -- if inserting
END; 





