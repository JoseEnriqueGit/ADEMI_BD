           );

ALTER TABLE PR.PR_TIPO_CREDITO_REPRESTAMO ADD (
  CONSTRAINT CK01_TIPO_CREDITO_REPRESTAMO
  CHECK (ESTADO in ('A','I'))
  ENABLE VALIDATE
,  CONSTRAINT PK01_TIPO_CRED_REPRESTAMO
  PRIMARY KEY
  (CODIGO_EMPRESA, TIPO_CREDITO)
  USING INDEX PR.PK01_TIPO_CRED_REPRESTAMO
  ENABLE VALIDATE);


CREATE OR REPLACE TRIGGER "PR"."TRG_BUI_TIPO_CRED_REPRESTAMO" 
BEFORE INSERT OR UPDATE
ON PR.PR_tipo_credito_REPRESTAMO REFERENCING NEW AS New OLD AS Old
FOR EACH ROW
BEGIN
  IF INSERTING THEN
     :new.fecha_adicion := sysdate;
     :new.estado := 'A';
  ELSE 
     :new.fecha_modificacion := sysdate;
  END IF;
END;
/


CREATE OR REPLACE PUBLIC SYNONYM PR_TIPO_CREDITO_REPRESTAMO FOR PR.PR_TIPO_CREDITO_REPRESTAMO;


ALTER TABLE PR.PR_TIPO_CREDITO_REPRESTAMO ADD (
  CONSTRAINT FK01_TIPO_CREDITO_REPRESTAMO 
  FOREIGN KEY (CODIGO_EMPRESA, TIPO_CREDITO) 
  REFERENCES PR.PR_TIPO_CREDITO (CODIGO_EMPRESA, TIPO_CREDITO)
  ENABLE VALIDATE);

