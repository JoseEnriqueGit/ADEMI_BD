CREATE TABLE CD_PRD_TASA_PLAZO_MONTO (
  COD_EMPRESA         VARCHAR2(5)       NOT NULL,
  COD_PRODUCTO        VARCHAR2(4)       NOT NULL,
  CODIGO              NUMBER(15)        NOT NULL,
  FECHA_VIGENCIA      DATE              NOT NULL,
  SPREAD              NUMBER(8,4)       NOT NULL,
  OPERACION           VARCHAR2(1)       NOT NULL,
  ESTADO              VARCHAR2(1)       NOT NULL,
  PLAZO_MINIMO        NUMBER(5)         NOT NULL,
  PLAZO_MAXIMO        NUMBER(5)         NOT NULL,
  MONTO_MINIMO        NUMBER(17,2)      NOT NULL,
  MONTO_MAXIMO        NUMBER(17,2)      NOT NULL,
  ADICIONADO_POR      VARCHAR2(10)      NOT NULL,
  FECHA_ADICION       DATE              NOT NULL,
  MODIFICADO_POR      VARCHAR2(10),     -- Puede ser NULL
  FECHA_MODIFICACION  DATE,             -- Puede ser NULL
  COD_TASA            VARCHAR2(5)       NOT NULL,
  TASA_MINIMA         NUMBER(10,6),     -- Puede ser NULL
  TASA_MAXIMA         NUMBER(10,6),     -- Puede ser NULL

  CONSTRAINT PK_CD_PRD_TASA
    PRIMARY KEY (COD_EMPRESA, COD_PRODUCTO, CODIGO, FECHA_VIGENCIA)
);

