CREATE TABLE CD_CUPON (
    COD_EMPRESA          VARCHAR2(5 BYTE) NOT NULL,
    NUM_CERTIFICADO      VARCHAR2(15 BYTE) NOT NULL,
    NUMERO_CUPON         NUMBER(3) NOT NULL,
    MONTO_BRUTO          NUMBER(17, 2),
    ESTADO               VARCHAR2(1 BYTE),
    MONTO_NETO           NUMBER(17, 2),
    FECHA_VENCIMIENTO    DATE,
    FECHA_PAGO           DATE,
    CONTADOR_IMPRESION   NUMBER(4),
    UTILIZADO            VARCHAR2(1 BYTE),
    EXCENTO              VARCHAR2(1 BYTE),
    ADICIONADO_POR       VARCHAR2(10 BYTE),
    FECHA_ADICION        DATE,
    MODIFICADO_POR       VARCHAR2(10 BYTE),
    FECHA_MODIFICACION   DATE,
    OBSERVACION          VARCHAR2(1024 BYTE),
    MON_RETENIDO         NUMBER(17, 2),
    PLA_DIAS             NUMBER(4),
    DIAS_CHEQUES         NUMBER(3),
    DIAS_EFECTIVO        NUMBER(3),
    CONSTRAINT PK_CD_CUPON PRIMARY KEY (COD_EMPRESA, NUM_CERTIFICADO, NUMERO_CUPON)
);
